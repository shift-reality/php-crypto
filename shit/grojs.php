function B64($n, $x) {
    if ($n === 7) {
        return $x->lo & 0xFF;
    }
    $bits = (7 - $n) * 8;
    if ($bits >= 32) { //faster than >= 32
        return ($x->hi >> ($bits - 32)) & 0xFF; ///?????????????????????????????
    } else {
        $bitsOff32 = 32 - $bits;
        $toMoveDown = $x->hi << $bitsOff32 >> $bitsOff32; ///?????????????????????????????
        return ($x->lo >> $bits | ($toMoveDown << $bitsOff32)) & 0xFF;
    }
}

