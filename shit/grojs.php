function groestl($ctx, $data, $len) {
    //var buf, ptr;
    //create a local copy of states
    $V = array_fill(0, 16, NULL); //16
    $buf = $ctx->buffer;
    $ptr = $ctx->ptr;
    if ($len < count($ctx->buffer) - $ptr) {
        bufferInsert($buf, $ptr, $data, count($data));
        $ptr += count($data);
        $ctx->ptr = $ptr;
        return;
    }
    //perform a deep copy of current state
    bufferInsert($V, 0, $ctx->state, 16);
    while ($len > 0) {
        $clen = count($ctx->buffer) - $ptr;
        if ($clen > $len)
            $clen = $len;
        bufferInsert($buf, $ptr, $data, $clen);
        $ptr += $clen;
        $data = array_slice($data, $clen);
        $len -= $clen;
        if ($ptr === count($ctx->buffer)) {
            $int64Buf = bytes2Int64Buffer($buf);

            compress($int64Buf, $V);
            $ctx->count->addOne();
            $ptr = 0;
        }
    }
    $ctx->state = $V;
    $ctx->ptr = $ptr;
}

function groestlClose($ctx) {
    $buf = $ctx->buffer;
    $ptr = $ctx->ptr;
    $pad = array_fill(0, 136, 0); //136
    $len = count($buf);
    $padLen = NULL;
    $count = NULL;
    $pad[0] = 0x80;
    if ($ptr < 120) {
        $padLen = 128 - $ptr;
        $count = $ctx->count->plus(o_u(0, 1));
    } else {
        $padLen = 256 - $ptr;
        $count = $ctx->count->plus(o_u(0, 2));
    }
    bufferSet($pad, 1, 0, $padLen - 9);
    bufferEncode64($pad, $padLen - 8, $count);
    groestl($ctx, $pad, $padLen);
    __final($ctx->state);
    $out = array_fill(0, 16, 0); //16
    for ($u = 0, $v = 8; $u < 8; $u++, $v++) {
        $out[2 * $u] = $ctx->state[$v]->hi;
        $out[2 * $u + 1] = $ctx->state[$v]->lo;
    }
    return $out;
}

